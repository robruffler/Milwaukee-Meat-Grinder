package com.fauxwerd.model;

public class Content {
	private Long id;
	private String url;
	
	public Content() { }

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}
	
}
